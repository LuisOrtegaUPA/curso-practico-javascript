// Declaración de Array

let frutas = ["mango", "fresa"];

// Métodos Arrays

frutas.length(); // Longitud del arreglo
frutas.push("melon"); // Agregar a cola un elemento
frutas.pop(); // Remover último elemento del arreglo
frutas.unshift("platano"); // Agrega elemento al principio del arreglo
frutas.shift(); // Remover primer elemento del arreglo
frutas.indexOf("platano"); // Muestra la posición del elemento que se desee
